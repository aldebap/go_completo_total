package main

import "fmt"

func main() {
	fmt.Printf("numero inteiro: %d\n", 10)
	fmt.Printf("numero ponto flutuante: %f\n", 3.141592653589)
	fmt.Printf("string: %s\n", "Hello, World!")
	fmt.Printf("booleano: %t\n", true)
	fmt.Printf("booleano: %t\n", false)
	fmt.Printf("ponteiro nulo: %v\n", nil)
}
